/**
 * Дані для інтерактивних шкал-калькуляторів (категорія «Шкали та інструменти»).
 *
 * STOP-BANG і ASA-PS наведені дослівно за українським перекладом джерела
 * (Передопераційна оцінка… додатки 1 та 4). AUDIT і шкали ризику PONV (Apfel,
 * POVOC) реконструйовані з канонічних валідованих інструментів; смуги балів
 * AUDIT — за документом. Дані суто статичні: підрахунок виконує клієнт офлайн.
 */

export type Severity = 'info' | 'success' | 'warning' | 'critical';

export interface ScoreOption {
  label: string;
  value: number;
}

export interface ScoreQuestion {
  /** Текст питання. */
  label: string;
  /** Необов'язкове уточнення під питанням. */
  hint?: string;
  options: ScoreOption[];
}

export interface ScoreBand {
  min: number;
  max: number;
  label: string;
  detail: string;
  severity: Severity;
}

export interface ScoreInstrument {
  id: string;
  /** Назва результату, напр. «Сума STOP-BANG». */
  title: string;
  /** Одиниця для табло, напр. «балів». */
  unit: string;
  /** true → кожне питання як пара «Ні / Так» (бінарне). */
  binary?: boolean;
  /** Примітка під карткою результату. */
  note?: string;
  questions: ScoreQuestion[];
  bands: ScoreBand[];
}


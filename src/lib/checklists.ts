/**
 * Дані чек-листів безпеки. Суто статичні: відмітки рахує клієнт у межах сесії.
 * Поки що — Хірургічний чек-лист безпеки ВООЗ (3 фази).
 */

export type ChecklistSeverity = 'info' | 'warning' | 'success';

export interface ChecklistItem {
  /** Текст пункту для відмітки. */
  text: string;
  /** Необов'язкове уточнення (пороги, дії) дрібним шрифтом. */
  note?: string;
}

export interface ChecklistPhase {
  id: string;
  /** Назва фази. */
  title: string;
  /** Коли виконується. */
  when: string;
  /** Хто бере участь. */
  team: string;
  severity: ChecklistSeverity;
  items: ChecklistItem[];
}

export interface SafetyChecklist {
  id: string;
  title: string;
  phases: ChecklistPhase[];
}

